function display() {
  if (document.getElementById('r1').checked) {
    document.body.style.background = "url(light.jpg) no repeat right top";
  }
  else if (document.getElementById('r2').checked) {
    document.body.style.background = "url(lens.jpg) no repeat right top";
  }
  else if (document.getElementById('r2').checked) {
    document.body.style.background = "url(creative.jpg) no repeat right top";
  }
  else if (document.getElementById('r2').checked) {
    document.body.style.background = "url(light.jpg) no repeat right top";
  }
}
